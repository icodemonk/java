package OPERATOR;

public class arthematic {


    //defination : Arthematic opertator is a opertator which used for addition subtraction division and multiplication and remainder
    // use case : + ,-,*,/,%
    public static void main(String[] args) {
        int num1 = 12 ;
        int num2 = 23 ;

        System.out.println("i am addition " + (num1+num2) );
        System.out.println("i am subtraction " + (num1-num2) );
        System.out.println("i am multiplication " + (num1 * num2) );
        System.out.println("i am division " + (num1 / num2) );
        System.out.println("i am remainder " +(num1%num2));
        System.out.println("i am incree num1 " + ++num1 );
        System.out.println(--num1);
   }

}
